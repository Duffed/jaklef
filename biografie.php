		<h2 class="headline">Biografie</h2>
		
		<p class="marginbottom">2001 gr&uuml;ndete Jaklef zusammen mit Daniel.H das Label <em class="highlight">Barake Records</em> in Koblenz / Neuendorf. 
		Der Name Barake kam zustande, da Jaklef im Hinterhof seines Elternhauses in einer kleinen 
		runtergekommen Baracke sein erstes eigenes Studio hatte. Die ersten 2 Jahre bastelten sie eifrig 
		an ihren eigenen Beats und Texten bis dann auch 2002 ihr erstes Album erschien. Das Album 
		wurde selbst produziert und vermarktet. Jaklef und Daniel.H, unter dem Namen Horn bekannt, 
		machten ihre ersten gemeinsamen Schritte in der Hip Hop Szene und traten auf einigen Jams 
		in Koblenz und Umgebeung auf. In den weiteren Jahren bekam Barake Records zahlreiche neue
		Mitglieder. Unter anderem Andres. M, bekannt als AMD, und Robert unter dem Namen Resor. 
		Es folgten in den n&auml;chsten Monaten noch weitere Rapper, wie z.B. Alex, der bekannt war unter 
		dem Namen Bubi. Die Jungs entschlossen sich dazu, ein Kolabo-Album zu machen und arbeiteten 
		die n&auml;chsten Monate eifrig an ihrem ersten gemeinsamen Projekt. Es folgte im Jahr 2003 das 
		Album <em class="highlight">Konflikt</em> mit zahlreichen Tracks. Das Album war wieder komplett selbst produziert und 
		vermarktet. Sie erstellten eigene Cover und Cds. Schnell machten die Jungs von Barake Records 
		sich in Koblenz und Umgebung einen Namen und waren nicht weiter &quot;Nonames&quot;. Es folgten die 
		weiteren Monate, zahlreiche Auftritte auf lokalen Jams auf denen sie unter anderem auch Vorgruppe
		f&uuml;r <em class="highlight">Azad aus Frankfurt und Kool Savas aus Berlin</em> waren. </p>
		
		<p class="marginbottom">2005 war Jaklef das erste Mal als Solo K&uuml;nstler unterwegs und auch gleich mit zwei Songs auf 
		dem 2. Koblenzer Hip Hop Sampler vertreten. Der Sampler wurde von Hoodflame in Zusammenarbeit mit dem Koblenzer Jugendamt produziert. </p>
		
		<p class="marginbottom">Jaklef war seit 2002 auch als Beatproduzent unter dem Namen <em class="highlight">BayziBeatz</em> t&auml;tig und Songschreiber f&uuml;r 
		den ein oder anderen K&uuml;nstler aus dem Raum Koblenz. Er teilte sich ein Studio mit 
		mehreren K&uuml;nstlern aus seiner Stadt und produzierte die n&auml;chsten Jahre unz&auml;hlige Tracks im 
		Rap- und Soulbereich.</p>
		
		<p class="marginbottom">Anfang 2009 entschloss sich Jaklef dann sein erstes Solo Album zu produzieren und bastelte fast 
		ein ganzes Jahr im Barake Records Studio mit <em class="highlight">Rico.L</em> und anderen K&uuml;nstlern wie z.B. <em class="highlight">T-Element, 
		Horn und Zek`man</em> an seinem Album. Ende 2009 erschien dann das Album unter dem Namen 
		<em class="highlight">"Made in Germany"</em>. Jaklef und seine Jungs veranstalteten auch eine Release-Party in Koblenz um 
		sein neues Album vorzustellen. Es folgte im n&auml;chsten Jahr auch sein erstes Solo Musik Video. Es 
		war eine Auskopplung aus seinem Album. Das erste Video wurde zum Track &quot;BayziJay&quot; gedreht 
		und auf YouTube ver&ouml;ffentlicht.</p>
		
		<p class="marginbottom">Im Jahr 2010 entschloss sich Jaklef einen Schritt weiter zu gehen und schloss sich mit dem Rapper 
		<em class="highlight">Hope</em> aus Koblenz zusammen und sie ver&ouml;ffentlichten mehrere gemeinsame Videos. Unter anderem 
		war Ihr erstes Video <em class="highlight">"Sound wie Beton"</em>, das in Koblenz und Umgebung einschlug wie eine Bombe. 
		Es folgten zahlreiche Tracks wie <em class="highlight">"Hymne der Strassen"</em> und <em class="highlight">"Richtung Sonne"</em> die auch sehr 
		gut in der Szene angenommen wurden. Ihr zweites Video war Ende 2010 mit dem Track &quot;Koblenz&quot; 
		das eine ganz neue Bewegung in Koblenz aufrollte. Jaklef &amp; Hope hatten sich schnell einen Namen 
		in der Koblenzer Rapszene gemacht und traten auch in den n&auml;chsten Monaten auf Jams und 
		Konzerten in der Umgebung auf.</p>
		
		<p class="marginbottom"><em class="highlight">Heute</em> ver&ouml;ffentlicht Jaklef zum gr&ouml;&szlig;ten Teil als Solok&uuml;nstler seine Werke. Doch arbeitet er auch 
		hin und wieder mit anderen K&uuml;stlern, wie z.B. F.A.T. Nurhak und Der Red von der Karthause in 
		Koblenz, T- Element aus Bad H&ouml;nningen bei Bonn, der wiederum zu Barake Records geh&ouml;rt. 2013 
		ver&ouml;ffentlichte Jaklef zusammen mit F.A.T. Zeitmaschiene, Frei, Erste gro&szlig;e Liebe und Feel. 
		Die komplett von Jaklef in Zusammenarbeit mit F.A.T. produziert wurden.</p>
		
		<p>Zur Zeit sind neue Tracks und dazugeh&ouml;rige Videos in Arbeit. Die n&auml;chsten Projekte die Jaklef
		als Solok&uuml;nstler erarbeitet werden zum gr&ouml;&szlig;ten Teil &uuml;ber das Label Barake Records ver&ouml;ffentlicht.</p>
